/* 
辛庄小学的李老师买了数十箱苹果，准备分给班里的同学，数了数人数，
刚好每人分10个还剩6个，正要分发时，又来了几位同学，
于是又重新分，每人分9个还剩4个，
最后全班同学都到齐了，每人只能分8个，还剩2个,
现在请你编写程序算一算李老师至少买了多少个苹果？
 */

#include <stdio.h>
int main()
{
    for (int i = 1; i < 1000; i++)
    {
        if (i % 10 == 6 && i % 9 == 4 && i % 8 == 2)
        {
            printf("%d\n", i);
            return 0;
        }
    }
    return 0;
}